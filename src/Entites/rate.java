package Entites;

/**
 *
 * @author mazen
 */
public class rate {
	private int id;
	private String rate;

	public rate() {
	}

	public rate(int id, String rate) {
		this.id = id;
		this.rate = rate;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getRate() {
		return rate;
	}

	public void setRate(String rate) {
		this.rate = rate;
	}

}
